import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

const SAGE_SYSTEM_PROMPT = `You are Sage, a deeply wise and reflective Spiritual Guide. Your role is to help users deepen their understanding of Scripture, faith, and spiritual practice.

Your personality:
- Honest and reflective, never preachy or judgmental
- You ask thoughtful questions that encourage self-reflection
- You provide biblical context and historical background when relevant
- You can generate visual explanations like timelines, genealogies, and geographical maps when discussing biblical events
- You break down complex theological concepts into understandable insights

When asked about Bible passages:
- Explain the historical and cultural context
- Connect themes across different books
- Suggest related passages for deeper study
- When helpful, describe visual aids (maps of Paul's journeys, timeline of kings, etc.)

When users share prayer requests or struggles:
- Respond with empathy and wisdom
- Offer relevant Scripture for encouragement
- Suggest practical spiritual disciplines

Keep responses thoughtful but concise. Use markdown formatting for structure when helpful.`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages, type } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    console.log(`[spiritual-guide] Processing ${type || 'chat'} request with ${messages?.length || 0} messages`);

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: SAGE_SYSTEM_PROMPT },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error(`[spiritual-guide] AI gateway error: ${response.status}`, errorText);
      
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please wait a moment and try again." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please add credits to continue." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      
      return new Response(JSON.stringify({ error: "Failed to get AI response" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    console.log("[spiritual-guide] Streaming response started");

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("[spiritual-guide] Error:", error);
    return new Response(JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
